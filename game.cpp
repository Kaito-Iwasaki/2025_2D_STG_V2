//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitUzawa();
	InitPlayer();
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitUzawa();
	UninitPlayer();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	UpdateUzawa();
	UpdatePlayer();

	if (GetKeyboardTrigger(DIK_RETURN))
	{
		SetScene(SCENE_ANOTHER);
	}
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	DrawUzawa();
	DrawPlayer();
}