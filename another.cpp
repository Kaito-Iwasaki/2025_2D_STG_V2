//=====================================================================
// 初期化処理
//=====================================================================
void InitAnother(void)
{
	InitUzawa();

	GetUzawa()->obj.size *= 0.5;
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitAnother(void)
{
	UninitUzawa();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateAnother(void)
{
	GetUzawa()->obj.rot.z += 0.1f;

	UpdateUzawa();

	if (GetKeyboardTrigger(DIK_RETURN))
	{
		SetFade(SCENE_GAME);
	}
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawAnother(void)
{
	DrawUzawa();
}